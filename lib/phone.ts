/**
 * Российские телефонные номера: нормализация к канону '+7XXXXXXXXXX' и
 * строгая валидация БЕЗ SMS-подтверждения (см. спецификацию gating —
 * обязательные поля регистрации).
 *
 * Канон дублирует CHECK-ограничение на public.profiles.phone
 * ('^\+7\d{10}$' и не все-цифры-одинаковые); клиентская проверка строже
 * (первая цифра, анти-последовательности), чтобы отсечь мусор до сабмита.
 */

/**
 * Приводит пользовательский ввод к канону '+7XXXXXXXXXX'.
 *
 * Принимает (после удаления пробелов, скобок и дефисов):
 * - '+7XXXXXXXXXX' (12 символов)
 * - '7XXXXXXXXXX'  (11 цифр)
 * - '8XXXXXXXXXX'  (11 цифр, привычный формат с восьмёркой)
 * - 'XXXXXXXXXX'   (10 цифр, начинается с 9 — мобильный без кода страны)
 *
 * Возвращает null, если ввод не удаётся однозначно привести к канону.
 */
export function normalizeRuPhone(raw: string): string | null {
  const cleaned = raw.trim().replace(/[\s().\-]/g, '');
  if (cleaned === '') return null;

  if (/^\+7\d{10}$/.test(cleaned)) return cleaned;
  if (/^7\d{10}$/.test(cleaned)) return `+${cleaned}`;
  if (/^8\d{10}$/.test(cleaned)) return `+7${cleaned.slice(1)}`;
  if (/^9\d{9}$/.test(cleaned)) return `+7${cleaned}`;

  return null;
}

/**
 * Строгая проверка КАНОНИЧЕСКОГО номера (результата normalizeRuPhone):
 * - формат '+7' + ровно 10 цифр;
 * - первая из 10 цифр ∈ {3, 4, 8, 9} (мобильные 9xx, городские коды 3xx/4xx/8xx);
 * - не все цифры одинаковые (например, +79999999999);
 * - не прямая/обратная последовательность цифр
 *   (1234567890, 0123456789, 9876543210).
 */
export function validateRuPhone(normalized: string): boolean {
  if (!/^\+7\d{10}$/.test(normalized)) return false;

  const digits = normalized.slice(2); // 10 цифр после '+7'

  if (!['3', '4', '8', '9'].includes(digits[0])) return false;

  // Все цифры одинаковые — мусор.
  if (/^(\d)\1{9}$/.test(digits)) return false;

  // Прямая/обратная последовательность из спеки — мусор. Ровно эти три:
  // более общая (wrap-aware) проверка отбраковывала бы реальные номера
  // вида +7 901 234-56-78 (код 901 существует).
  if (['1234567890', '0123456789', '9876543210'].includes(digits)) return false;

  return true;
}
